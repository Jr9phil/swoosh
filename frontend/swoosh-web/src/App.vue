<script setup lang="ts">
import { useAuthStore } from './stores/auth'
import { useRouter } from 'vue-router'

const auth = useAuthStore()
const router = useRouter()

function logout() {
  auth.logout()
  router.push('/login')
}
</script>

<template>
  <div>
    <header style="margin-bottom: 1rem">
      <button v-if="auth.token" @click="logout">
        Logout
      </button>
    </header>

    <router-view />
  </div>
</template>

